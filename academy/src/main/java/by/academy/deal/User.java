package by.academy.deal;

public class User {
		
	private String name;
	private double money;
	
	public User () {
		super();
	}	
	public void setName (String name) {
		this.money=money;
	}
	public String getName () {
		return name;
	}
	public void setMoney (double money) {
		this.money=money;
	}
	public double getMoney () {
		return money;
	}
	
}
